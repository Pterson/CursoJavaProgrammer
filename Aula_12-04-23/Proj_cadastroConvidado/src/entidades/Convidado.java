package entidades;

public class Convidado {
	
		private String nome;
		private String cpf;
		
//____________GETTERS e SETTERS____________		
		
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public String getCpf() {
			return cpf;
		}
		public void setCpf(String cpf) {
			this.cpf = cpf;
		}
}
